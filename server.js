const myNFTContract = new Web3.eth.Contract(contractABI, contractAddress);
// Handle APIs
app.get('/nft/:tokenId', async (req, res) => {
    const tokenId = req.params.tokenId;
    try {
        // Call the getNFT function
        const nftData = await myNFTContract.methods.getNFT(tokenId).call();

        // Return the NFT data
        res.json({
            name: nftData[0],
            metadataURI: nftData[1]
        });
    } catch (error) {
        console.error(error);
        res.status(500).json({ error: 'Internal Server Error' });
    }
});

